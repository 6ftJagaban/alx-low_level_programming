#include <stdio.h>
/**
 * main - ALX Coding Project
 * DESCRIPTION - nmbers in base 16, hexadecimals
 * Return: Always 0 (Success)
 */
int main(void)
{
	char f;

	int r;

	f = 'a';
	r = 0;
	while
		(r < 10) {
			putchar(r + '0');
			r++;
		}
	while
		(f <= 'f') {
			putchar(f);
			f++;
		}
	putchar('\n');
	return (0);
}
