#include <stdio.h>
/**
 * main - ALX Coding Project
 * DESCRIPTION - printing alphabets in lowercase using 'putchar' function only
 * Return: Always 0 (Success)
 */
int main(void)
{
	char t;

	t = 'a';
	while
		(t <= 'z') {
			putchar(t);
			t++;
		}
	putchar(t);


	putchar('\n');

	return (0);



}
